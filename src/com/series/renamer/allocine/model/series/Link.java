
package com.series.renamer.allocine.model.series;


public class Link{
   	private String href;
   	private String rel;

 	public String getHref(){
		return this.href;
	}
	public void setHref(String href){
		this.href = href;
	}
 	public String getRel(){
		return this.rel;
	}
	public void setRel(String rel){
		this.rel = rel;
	}
}
